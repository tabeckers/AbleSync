namespace AbleSync.Core.Types
{
    /// <summary>
    ///     Represents the format of an audio file.
    /// </summary>
    public enum AudioFormat
    {
        /// <summary>
        ///     .mp3 file format.
        /// </summary>
        Mp3 = 0,

        /// <summary>
        ///     .wav file format.
        /// </summary>
        Wav = 1,

        /// <summary>
        ///     .flac file format.
        /// </summary>
        Flac = 2
    }
}
